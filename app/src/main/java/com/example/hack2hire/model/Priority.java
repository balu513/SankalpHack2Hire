package com.example.hack2hire.model;

public class Priority {

    private int id;

    private String refName;

    public void setId(int id) {
        this.id = id;
    }

    public void setRefName(String refName) {
        this.refName = refName;
    }

    public int getId() {
        return id;
    }

    public String getRefName() {
        return refName;
    }
}
